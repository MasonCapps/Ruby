#JavaScript Symbol Syntax
item1 = { name: water, color: clear, price: 2 }
item2 = { name: candle, color: blue, price: 10 }
item3 = { name: carpet, color: brown, price: 20 }

#Ruby Symbol Syntax
item1 = { :name => water, :color => clear, :price => 2 }
item2 = { :name => candle, :color => blue, :price => 10 }
item3 = { :name => carpet, :color => brown, :price => 20 }
